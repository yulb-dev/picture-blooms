

export function OpenrequestBox() {
    let box = document.getElementById('beforerequestBox');
    if (box) {
        box.style.display = 'block'
    }

}

export function CLoserequestBox() {
    let box = document.getElementById('beforerequestBox');
    if (box) {
        box.style.display = 'none'
    }
}

